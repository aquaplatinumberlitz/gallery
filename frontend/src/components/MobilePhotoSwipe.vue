<script setup lang="ts">
import { ref, onMounted, onUnmounted, watch, computed } from "vue";
import PhotoSwipe from "photoswipe";
import "photoswipe/dist/photoswipe.css";
import type { FileNode } from "../types";
import { getThumbnailUrl } from "../services/api";

const props = defineProps<{
  items: FileNode[];
  currentIndex: number;
  isOpen: boolean;
  metadataOpen?: boolean;
}>();

const emit = defineEmits<{
  (e: "close"): void;
  (e: "indexChange", index: number): void;
  (e: "toggleMetadata"): void;
}>();

const containerRef = ref<HTMLElement | null>(null);
let pswp: PhotoSwipe | null = null;

// Build PhotoSwipe data source from our items
const pswpItems = computed(() =>
  props.items.map((item) => {
    // Use a large thumbnail URL for mobile — PhotoSwipe handles sizing.
    // Store original path for metadata lookup after index change.
    return {
      src: getThumbnailUrl(item.path, 1600),
      width: 1200,
      height: 1600,
      alt: item.name || "",
      path: item.path,
    };
  })
);

function initPhotoSwipe() {
  if (!containerRef.value || !props.isOpen || pswp) return;

  pswp = new PhotoSwipe({
    dataSource: pswpItems.value,
    index: props.currentIndex,
    pswpModule: () => Promise.resolve(PhotoSwipe),
    appendToEl: containerRef.value,
    // Let PhotoSwipe handle swipe-down-to-close
    closeOnVerticalDrag: true,
    showHideAnimationType: "zoom",
    allowPanToNext: true,
    wheelToZoom: false,
    bgOpacity: 1,
  });

  // Listen for index changes → emit so parent can fetch metadata
  pswp.on("change", () => {
    if (pswp) {
      emit("indexChange", pswp.currIndex);
    }
  });

  // Listen for close (swipe-down, escape key, etc.)
  pswp.on("close", () => {
    destroyPhotoSwipe();
    emit("close");
  });

  // Register info button as a proper PhotoSwipe 5 UI element (mobile)
  pswp.on("uiRegister", () => {
    pswp!.ui!.registerElement({
      name: "metadata-info",
      order: 9,
      isButton: true,
      html: {
        isCustomSVG: true,
        inner:
          '<path d="M12 11v6" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round"/><path d="M12 7h.01" fill="none" stroke="currentColor" stroke-width="3" stroke-linecap="round"/>',
        size: 24,
      },
      onInit: (el: HTMLElement) => {
        el.classList.add("pswp__button--metadata-info");
        if (props.metadataOpen) {
          el.classList.add("active");
          el.setAttribute("aria-label", "Close image info");
        } else {
          el.setAttribute("aria-label", "View image info");
        }
      },
      onClick: () => emit("toggleMetadata"),
    });
  });

  pswp.init();

  // After init, move the info button outside .mobile-photoswipe-container
  // into .lightbox-overlay so it can stack above the bottom sheet
  const infoBtn = document.querySelector<HTMLElement>(
    ".pswp__button--metadata-info"
  );
  if (infoBtn) {
    infoBtn.classList.remove("pswp__hide-on-close");
    const overlay = document.querySelector<HTMLElement>(".lightbox-overlay");
    if (overlay) {
      overlay.appendChild(infoBtn);
    }
  }
}

function destroyPhotoSwipe() {
  if (pswp) {
    try {
      pswp.destroy();
    } catch (_) {
      // Already destroyed
    }
    pswp = null;
  }
}

// Watch isOpen — init when opening, destroy when closing
watch(
  () => props.isOpen,
  (open) => {
    if (open) {
      // Small delay to ensure DOM is ready
      setTimeout(() => initPhotoSwipe(), 0);
    } else {
      destroyPhotoSwipe();
    }
  }
);

// Watch currentIndex — sync PhotoSwipe if instance exists
watch(
  () => props.currentIndex,
  (index) => {
    if (pswp && pswp.currIndex !== index) {
      pswp.goTo(index);
    }
  }
);

// Watch metadataOpen — toggle active state and visibility on the info button
watch(() => props.metadataOpen, (isOpen) => {
  const btn = document.querySelector<HTMLElement>(
    ".pswp__button--metadata-info"
  );
  if (btn) {
    btn.classList.toggle("active", !!isOpen);
    btn.classList.toggle("hidden", !!isOpen);
    btn.setAttribute("aria-label", isOpen ? "Close image info" : "View image info");
  }
});

onMounted(() => {
  if (props.isOpen) {
    initPhotoSwipe();
  }
});

onUnmounted(() => {
  destroyPhotoSwipe();
});
</script>

<template>
  <div ref="containerRef" class="mobile-photoswipe-container"></div>
</template>

